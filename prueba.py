plt.plot(data_01[:,0][:-1], data_01[:,1][:-1], label="Delta = " + str(data_01[:,0][-1]))
plt.plot(data_001[:,0][:-1], data_001[:,1][:-1], label="Delta = " + str(data_001[:,0][-1]))
plt.xlabel('t')
plt.ylabel('Y')
plt.legend()
plt.tight_layout()
